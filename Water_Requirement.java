import java.util.*;

/**
 * Water_Requirement
 */
public class Water_Requirement {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int t = sc.nextInt();

        for (int i = 0; i < t; i++) 
        {
            int n = sc.nextInt();
            
            n = 2 * n;
            System.out.println(n);
        }
    }
}